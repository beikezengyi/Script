#	sh defreeze.sh
#-------------------------------------------------------------------------------

#:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::
#	init
#:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::

cd `dirname $0`

# 共通設定
LISTDIR=/var/opt/FJSVoast/assets

BASEDIR=`pwd`

TIMESTAMP=`date '+%Y%m%d.%H%M%S'`

if [[ ! -e backup ]]; then
	mkdir backup
fi

#backup remove
#直近一回のBKだけ取る
rm -rf backup/*.tar

#:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::
#	main
#:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::

#bk
tar cf backup/$TIMESTAMP.tar $LISTDIR

#解凍
tar Pxvf dist/report-form.tar.gz

